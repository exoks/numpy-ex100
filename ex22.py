# ===[ Import: ]===============================================================
import numpy as np


# ===[ main: ]=================================================================
def main():
    # Creating a 5x5 matrix/2D array with random number
    matrix = np.random.randint(1, 20, (5, 5))

    print("===========[Normalization (range --- max - min )]===============")
    # Method 1: Normalization (using max - min, range)
    rnge = matrix.max(axis=0) - matrix.min(axis=0)
    normalized = (matrix - matrix.min(axis=0)) / rnge
    np.set_printoptions(precision=2)
    print(normalized)

    print("=======[Normalization (standardization z-score)]================")
    mean = matrix.mean(axis=0)
    std = matrix.std(axis=0)
    print(f"mean by column: {mean}")
    print(f"standard diviation by column: {std}")
    z_score = (matrix - mean) / std
    print(f"z-score: {z_score}")

    # NOTE:-------------------------------------------------------------------
    # - There is also IQR 'InterQuartil Range' method to normalize the matrix
    # - This EX is about statistical operations (mean, range, std, z-score)


if __name__ == '__main__':
    main()
