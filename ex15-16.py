import numpy as np


# ===[ main ]==================================================================
def main():
    # Creating an array with '1' in the border and 0
    # 1. creating a 2D array/Matrix fillted with ones
    print("============[slicing]==============")
    matrix = np.ones(shape=(4, 4), dtype=np.int64)
    # 2. slice the matrix's inside and change their values to 0
    matrix[1:-1, 1:-1] = 0
    # Or
    # matrix[1:matrix.shape[0] - 1, 1:matrix.shape[1] - 1] = 0
    print(f"matrix:\n{matrix}")

    # NOTE:--------------------------------------------
    # This EX is about indexing and slicing a 2D array

    # 16. Using built-in np.pad() function to add padding to the existing
    # matrix, (we are adding 0 in the border)
    print("============[np.pad()]==============")
    matrix = np.pad(matrix, pad_width=1, mode='constant', constant_values=4)
    print(f"matrix:\n{matrix}")

    # NOTE:----------------------------------------------
    # np.pd() is used to add padding to an existing array


if __name__ == '__main__':
    main()
