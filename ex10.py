import numpy as np


# ===[ main ]==================================================================
def main():
    # finding indices of non-zeror elements from vector [1, 2, 0, 0, 4, 0]
    vector = np.array([1, 2, 0, 0, 4, 0])
    # Method 1: Using Comprhension list
    print("=====Using Comprehension list======")
    indeces = np.array([index for index in range(
        vector.size) if vector[index] != 0])
    print(f"indeces: {indeces}")
    # NOTE: This method is not recommonded, Always try to use np lib because
    #       It is fast

    # Method 2:
    print("=====Using np.where()=====")
    # It does return a tupple of ndarray
    indeces = np.where(vector != 0)
    print(f"indeces: {indeces[0]}")


if __name__ == '__main__':
    main()
